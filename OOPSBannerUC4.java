public class OOPSBannerUC4 {
    public static void main(String[] args){
        String[][] banner = {
                { "   ***   ", "   ***   ", " ******  ", "   ***** " },
                { " **   ** ", " **   ** ", " **   ** ", "  **     " },
                { " **   ** ", " **   ** ", " **   ** ", "  **     " },
                { " **   ** ", " **   ** ", " ******  ", "   ***   " },
                { " **   ** ", " **   ** ", " **      ", "     **  " },
                { " **   ** ", " **   ** ", " **      ", "      ** " },
                { " **   ** ", " **   ** ", " **      ", "      ** " },
                { "  **  ** ", "  **  ** ", " **      ", "     **  " },
                { "   ***   ", "   ***   ", " **      ", " *****   " }
       };
        for (String[] row : banner) {
            System.out.println(String.join(" ", row));
        }
    }
}
    

